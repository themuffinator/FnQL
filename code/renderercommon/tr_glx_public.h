#define GLX_MATERIAL_STATE_SRCBLEND_SRC_ALPHA 0x00000005u
#define GLX_MATERIAL_STATE_SRCBLEND_ONE_MINUS_SRC_ALPHA 0x00000006u
#define GLX_MATERIAL_STATE_SRCBLEND_DST_ALPHA 0x00000007u
#define GLX_MATERIAL_STATE_SRCBLEND_ONE_MINUS_DST_ALPHA 0x00000008u
#define GLX_MATERIAL_STATE_SRCBLEND_ALPHA_SATURATE 0x00000009u
