/*
 * These mirror the compatibility renderer's GLS_* stage state bits. GLx keeps
 * the raw state word in material keys, but exposes the fields here so pure GLx
 * GLSL permutation logic can distinguish blend, depth, and alpha-test variants
 * without including renderer-private headers.
 */
